# 입력받는 자연수 N
N = int(input())

# blst 리스트를 N+2 크기로 만들어줌
blst = [0] * (N+2)

# N이 1일 때는 1개 (1)
# N이 2일 때는 2개 (00, 11)
blst[1] = 1
blst[2] = 2
    
# N이 3이상일 때, blst[i]는 blst[i-1] + blst[i-2]의 관계 만족
# 원하는 출력값이 blst[N]을 15746으로 나누어주는 나머지인데, 모든 값을 다 구하고 마지막에 나눠주는 것보다 매 연산마다 나머지 값을 구해서 적용시켜도 값에 변화가 없으며 훨씬 빠른 연산이 가능함
for i in range(3, N+1):
    blst[i] = (blst[i-1] + blst[i-2]) % 15746


print(blst[N])
