N = int(input())

# 1번 배열 가능한 수 : 1 + 2
# 2번 배열 가능한 수 : 3 + 2+2
# 3번 배열 가능한 수 : 7 + 3*2+4 = 17
# 4번 배열 가능한 수 : 17 + 7*2+10 = 41

# [i행에 한 마리도 배치하지 않는 경우, i행에 한 마리를 배치하는 경우]를 원소로 하는 리스트 dp
dp = [[1, 2]]
# dp[i] = [sum(dp[i-1]), dp[i-1][0]*2+dp[i-1][1]]
# 9901로 나눈 나머지를 구할 것이므로 연산에도 나머지를 담아줌
for i in range(1, N):
    dp.append([sum(dp[i-1])%9901, (dp[i-1][0]*2+dp[i-1][1])%9901])

print(sum(dp[N-1])%9901)